-- TABLE: ContactMessages (Chat replies for Contact)
-- ?????????????????????????????????????????????
CREATE TABLE ContactMessages (
    Id INT PRIMARY KEY IDENTITY(1,1),
    ContactId INT NOT NULL,                  -- Link to the original contact inquiry
    SenderType VARCHAR(50) NOT NULL,         -- 'user' or 'admin'
    SenderEmail VARCHAR(255) NULL,           -- Optional: who sent it
    MessageContent NVARCHAR(MAX) NOT NULL,   -- The actual message
    MessageDate DATETIME DEFAULT GETDATE(),  -- When it was sent
    FOREIGN KEY (ContactId) REFERENCES Contact(Id)
);

-- Index for performance
CREATE INDEX IX_ContactMessages_ContactId ON ContactMessages(ContactId);

