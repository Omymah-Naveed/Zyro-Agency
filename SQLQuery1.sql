-- =========================================
-- 3. BlogComments Table (nested)
-- =========================================
CREATE TABLE BlogComments (
    Id INT PRIMARY KEY IDENTITY(1,1),
    BlogId INT NOT NULL,                       -- FK to Blog
    UserId INT NOT NULL,                       -- FK to Registeration_User
    CommentText NVARCHAR(MAX) NOT NULL,        -- The comment content
    CommentDate DATETIME DEFAULT GETDATE(),    -- When it was posted
    ParentCommentId INT NULL,                  -- For nested replies (self FK)

    CONSTRAINT FK_BlogComments_Blog FOREIGN KEY (BlogId)
        REFERENCES Blog(Id) ON DELETE CASCADE,

    CONSTRAINT FK_BlogComments_User FOREIGN KEY (UserId)
        REFERENCES Registeration_User(Id) ON DELETE CASCADE,

    CONSTRAINT FK_BlogComments_Parent FOREIGN KEY (ParentCommentId)
        REFERENCES BlogComments(Id)
        ON DELETE NO ACTION   -- prevent multiple cascade paths
);

-- Indexes
CREATE INDEX IX_BlogComments_BlogId ON BlogComments(BlogId);
CREATE INDEX IX_BlogComments_UserId ON BlogComments(UserId);
CREATE INDEX IX_BlogComments_ParentId ON BlogComments(ParentCommentId);

